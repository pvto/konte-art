    private void replaceAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_replaceAllActionPerformed
        target.setCaretPosition(0);
        int cnt = 0;
        String pattern = findF.getText();
        if (pattern.length() == 0)
            return;
        int ind = target.getCaretPosition();
        int count = 0;
        for(;;)
        {
            ind = findNext(ind, true, pattern);
            if (ind >= 0)
            {
                ind = end;
                count++;
            } else
            if (cnt++ > 0)
                break;
        }
        org.konte.generate.Runtime.sysoutln("replaced " + count + " occurences");
        target.setCaretPosition(0);
}//GEN-LAST:event_replaceAllActionPerformed

