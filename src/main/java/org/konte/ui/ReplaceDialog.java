    private int end = 0;
    private int findNext(int pos, boolean replace, String text)
    {
        String orig = target.getText();
        if (this.jCheckBox1.isSelected())
        {
            Pattern p = Pattern.compile(text);
            Matcher m = p.matcher(orig);
            if (m.find(pos))
            {
                int replaceL = replaceF.getText().length();
                if (replace)
                {
                    target.setText(
                            orig.substring(0, m.start()) +
                            replaceF.getText() +
                            orig.substring(m.end())
                        );
                }
                end = m.end();
                return m.start();
            }
            end = 0;
            return -1;
            
        } 
        int pos2 = orig.indexOf(text, pos);
        if (pos2 >= 0)
        {
            if (replace)
            {
                target.setText(
                        orig.substring(0, pos2) +
                        replaceF.getText() +
                        orig.substring(pos2 + text.length())
                    );
                end = pos2 + replaceF.getText().length();
            }
            else
            {
                end = pos2 + text.length();
            }
            return pos2;
        }
        return -1;
    }
